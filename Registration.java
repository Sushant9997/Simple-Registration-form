    private void submitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitActionPerformed
         String Id=sid.getText();
         String Name=sname.getText();
         String Clas=sclass.getText();
         String Dept=sdept.getText();
         String Faculty=sfaculty.getText();
         String College=scollege.getText();
         String Phone=sphone.getText();
         
         
         if(Id.equals("")){
        JOptionPane.showMessageDialog(null, "ID is Mandatory.");
    }
         if(Name.equals("")){
        JOptionPane.showMessageDialog(null, "Name is Mandatory.");
    }
         if(Clas.equals("")){
        JOptionPane.showMessageDialog(null, "Class is Mandatory.");
    }
         if(Dept.equals("")){
        JOptionPane.showMessageDialog(null, "Department is Mandatory.");
    }
         if(Faculty.equals("")){
        JOptionPane.showMessageDialog(null, "Faculty is Mandatory.");
    }
         if(College.equals("")){
        JOptionPane.showMessageDialog(null, "College is Mandatory.");
    }
         if(Phone.equals("")){
        JOptionPane.showMessageDialog(null, "Phone is Mandatory.");
    }
         else{
             Connection conn = null;
             PreparedStatement st  = null;
            // JOptionPane.showMessageDialog(null,"Validation Access");
             try{
                // Class.forName("com.mysql.jdbc.Driver");
                 conn = DriverManager.getConnection("jdbc:mysql:///registration","root","computerscience");
                 String query ="Insert into details(Id,Name,Clas, Dept,Faculty,College,Phone) values (?,?, ?, ?,?, ?, ?)";
                 
                 st=conn.prepareStatement(query);
                 st.setString(1, Id);
                 st.setString(2, Name);
                 st.setString(3, Clas);
                 st.setString(4, Dept);
                 st.setString(5, Faculty);
                 st.setString(6, College);
                 st.setString(7, Phone);
                 st.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Data Entered");
          
             }catch(SQLException e)
             {
                 JOptionPane.showMessageDialog(null,e);
             } 
         }
    }//GEN-LAST:event_submitActionPerformed

