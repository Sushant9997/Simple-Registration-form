    private void sidKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_sidKeyReleased
        // TODO add your handling code here:
        // Email Pattern: - sushantagarwal78@gmail.com
        // String PATTERN = "^[a-zA-Z0-9]{0,20}@[a-zA-Z]{0,10}[.][a-zA-Z]{0,5}$";
           String PATTERN = "^[0-9]{0,6}$";
           Pattern patt=Pattern.compile(PATTERN);
           Matcher match = patt.matcher(sid.getText());
           if(!match.matches())
           {
               idlab.setText("ID is incorrect!");
           }
           else
           {
               idlab.setText(null);
           }
    
    }//GEN-LAST:event_sidKeyReleased

